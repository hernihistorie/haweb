    <p>Po asi dvou měsíčním hledáním se nám zadařilo nejen získat kontakt na autora slavné hry z pořadu Videostop, ale také se nám povedlo s ním včera udělat rozhovor a DOKONCE od něj i získat kazetu (nebo přesněji dvě) se samotnou hrou na ZX Spectrum (kterou za nedlouho najdete buď v našem inventáři nebo na cs.speccy).
    <p>Samotný rozhovor má ale žel dvě hodiny a jeho přepis tedy bude nějakou dobu trvat. Chtěli bychom Vás touto cestou tedy i poprosit a zeptat se, jestli by náhodou někdo neměl zájem nám pomoci s přepisem těchto historických rozhovorů. Dlouhodobě totiž bojujeme s nedostatkem přepisovatelské síly, která žel nejde zcela nahradit jakoukoliv automatizací či AIčkem. Pokud by se našel nějaký zájemce, který by nám chtěl pomoci s naším archivářským a historiografickým úsilím, budeme rádi, když se nám ozve.
