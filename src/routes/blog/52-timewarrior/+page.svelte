    <p>Time Warrior je hra pro ZX Spectrum z roku 1992 a později pro Amigu z roku 1997, kterou vytvořil anglický autor Frank Oliver. Odehrává ve fantaskním světě inspirované jeho trilogií knih a k příběhům vznikl i hudební soundtrack. Přestože se většinou věnujeme místní tvorbě, tak hru Time Warrior sháněl pro projekt eXoIF kolega z Rakouska a kontaktoval Franka Olivera, tkerý mu poslal CD které vytvořil v roce 2016 s kompilací vlastní tvorby včetně plné verze Time Warriora. A vzhledem k povolení sdílet obsah dál vytvořil zálohu a umístil ji na Internet Archive. Pro přidání do databáze herních disků Redump by ale bylo potřeba vytvořit zálohu archivní kvality na mechanikách Plextor. Dohodli jsme se tedy, že CDčko zazálohujeme my. Hra je tak přidaná na <a href="http://redump.org/disc/125756/">Redump</a> a můžete si ji stáhnout na <a href="https://archive.org/details/Time-Warrior-Starlight">archive.org</a>. Můžete také podpořit autora, který sídlí na webu <a href="https://shadesofevil.com/">ShadesOfEvil</a>.
