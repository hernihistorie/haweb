    <p>Herní historií nejsou jen hry a oficiální hardware – součástí herní historie (zejména té jenž se odehrávala v rámci bývalého Východního bloku) je i podomácku zbastlené příslušenství, jako např. herní joysticky. Ty často bývají velmi jednoduché a vyráběné z toho "co dům dal", nicméně nedávno se nám do rukou dostali joysticky, které nejde nazvat jinak, než vymazlené. Mají krásný kovovo-dřevný bytelný case, vlastní adaptér do ZX Spectra, zesilovač pro nahrávání zaznamenávání a jedna z páček je dokonce programovatelná, aby se s ni dali hrát hry, které je možné hrát jen s klávesnicí.

