    <p>Oficiálně oznamujeme, že jsme se stali členy European Federation of Game Archives, Museums and Preservation Projects (EFGAMP), který reprezentuje a propojuje více než 20 evropských institucí a organizací, jež se zaměřují na záchranu evropské herní kultury. Je nám ctí, že touto cestou můžeme sdílet s i přebýrat od jiných institucí know-how, podílet se na celoevropské snaze profesionalizovat herní prezervaci a také v rámci zbytku Evropy reprezentovat unikátní československou herní historii.   
