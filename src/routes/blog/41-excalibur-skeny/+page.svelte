    <p>Časopis Excalibur, jehož nulté číslo vyšlo v roce 1990, byl jedním z prvních časopisů o videohrách, který u nás vycházel a v kterém zároveň začínalo mnoho významných osobnostní herní žurnalistiky jako Jakub Červinka, Jan Eisler, Andrej Anastasov, Tomáš Mrkvička, Jan Tománek ad.
    <p>Časopis jsme oskenovali téměř celý, ale několik čísel nám bohužel ještě chybí. Konkrétně jde o čísla: 0-10, 18+, 20+, 64, 66, 67, 69-72. Pokud by se náhodou našel někdo, kdo by byl ochotný se vzdát jednoho z výše zmíněných čísel, tak bychom byli velice vděčný (Konktaktujte nás na <a href="mailto:info@herniarchiv.cz">info@herniarchiv.cz</a>.)
    <p>Oskenované Excalibury najdete všechny na <a href="https://archive.org/details/excalibur-magazine">Internet Archive</a>.
    <p>A databázi s Excalibury zase v naší <a href="https://casopisy.herniarchiv.cz/public-magdb/catalog/magazine-detail/13">Databázi časopisů</a>.
