    <h2>Služby a technologie</h2>
    <p>
        V rámci našich archivářských činností si klademe za cíl uchovávat materiály a data za pomocí těch nejlepších technologií a nejvyšších současných standardů. Například klademe důraz na zálohování nejen dat, ale i metadat, jako je zvolená metoda archivace a další okolnosti. Fyzické materiály skenujeme v nejlepší možné kvalitě a bez ztrátové komprese. Analogová a digitální média zálohujeme pomocí specializovaného hardwaru a dokonce se podílíme na testování a vývoji nových řešení. Kde je to na místě přispíváme výsledky naší činnosti do mezinárodních databází jako je <a href="http://redump.org/">Redump</a> či <a href="https://no-intro.org/">No-Intro</a>. Všechny původní kopie a fyzické artefakty uchováváme v pečlivě inventarizovaném archivu v suchu a za stabilizované teploty.
    </p>

    <p>
        Žádné médium není dlouhodobě udržitelné a vhodné na archivaci. Média jako optické mechaniky a pevné disky v průběhu času ztrácejí data (tzv. bitrot); u dedikovaných zálohovacích technologií jako jsou kazety se ztrácí podpora hardwaru, který jej umí číst; a i v případě zachovalých digitálních nosičů může být obnovení dat ze zastaralých formátů složité. Dnes je ten nejvyšší čas data zdigitalizovat a vytvořit nové kopie.
    </p>
    <p>
        Ke každému jednotlivému artiklu přistupujeme s největší pečlivostí, a zároveň se běžně podílíme na <a href="/projects">rozsáhlých archivačních projektech ve spřátelených institucích</a>. Rádi pomůžeme <strong>právě Vám</strong> zachránit data z obskurních médií, či zorganizovat projekt na digitalizaci celého archivu. Neváhejte a <a href="/contact"><strong>kontaktujte nás</strong></a>.
    </p>

